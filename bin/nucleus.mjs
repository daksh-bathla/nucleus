#!/usr/bin/env node
import { readFile, readdir, stat, writeFile, mkdir } from "node:fs/promises";
import { existsSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join, resolve, relative, extname } from "node:path";

import { validateUsage } from "../scripts/lib/validate-markup.mjs";

const HERE = dirname(fileURLToPath(import.meta.url));
const PKG_ROOT = resolve(HERE, "..");
const SCAN_EXTS = new Set([".html", ".htm", ".jsx", ".tsx", ".js", ".ts", ".vue", ".svelte", ".astro", ".php", ".erb"]);
const IGNORE_DIRS = new Set(["node_modules", ".git", "dist", ".next", "build", "out", "coverage", ".nucleus"]);
const AI_TOOL_ALIASES = new Map([
  ["claude", "claude"],
  ["claude-code", "claude"],
  ["codex", "codex"],
  ["openai", "codex"],
  ["cursor", "cursor"],
  ["antigravity", "antigravity"],
  ["agy", "antigravity"],
]);
const GENERATED_RULE_MARKER = "<!-- Generated by @navital/nucleus-css. Re-run ai-setup to refresh. -->";
const NUCLEUS_RULES = `# Nucleus CSS

- Before writing UI markup, read \`.nucleus/ai-context.md\`.
- Use only documented \`n-\`-prefixed Nucleus classes.
- Prefer canonical classes over aliases marked deprecated in the manifest.
- Use accurate native/ARIA state first; do not add unprefixed \`.active\` to new markup.
- Do not infer spacing from Tailwind: \`n-p-4\` is \`1.5rem\`.
- Query a component example before inventing its internal structure.
- Never substitute Tailwind, Bootstrap, or invented utility classes.
- After changing markup, run \`npx @navital/nucleus-css validate ./src ./app ./components ./pages\` and fix every reported class.
`;

async function loadManifest() {
  const path = join(PKG_ROOT, "dist", "nucleus.manifest.json");
  if (!existsSync(path)) {
    console.error("✗ dist/nucleus.manifest.json not found. Run `npm run build` first.");
    process.exit(1);
  }
  return JSON.parse(await readFile(path, "utf8"));
}

async function readPkg() {
  return JSON.parse(await readFile(join(PKG_ROOT, "package.json"), "utf8"));
}

async function collectFiles(target) {
  const info = existsSync(target) ? await stat(target) : null;
  if (!info) return [];
  if (info.isFile()) return [target];
  const out = [];
  for (const entry of await readdir(target)) {
    if (IGNORE_DIRS.has(entry)) continue;
    const full = join(target, entry);
    const s = await stat(full);
    if (s.isDirectory()) out.push(...(await collectFiles(full)));
    else if (SCAN_EXTS.has(extname(entry))) out.push(full);
  }
  return out;
}

async function cmdValidate(args) {
  const targets = args.length ? args : ["."];
  const manifest = await loadManifest();

  const files = [];
  for (const t of targets) files.push(...(await collectFiles(resolve(process.cwd(), t))));

  let errors = 0;
  let warnings = 0;
  let scanned = 0;
  for (const file of files) {
    const content = await readFile(file, "utf8");
    scanned += 1;
    const findings = validateUsage(content, manifest);
    for (const f of findings) {
      if (f.level === "error") errors += 1;
      else warnings += 1;
      const rel = relative(process.cwd(), file);
      console.error(`${f.level === "error" ? "Error" : "Warning"}: ${f.message}`);
      if (f.suggestion) console.error(`  Did you mean: ${f.suggestion}?`);
      if (f.replacement) console.error(`  Use instead: ${f.replacement}`);
      console.error(`  File: ${rel}`);
      console.error(`  Line: ${f.line}`);
      console.error("");
    }
  }

  if (errors === 0 && warnings === 0) {
    console.log(`✓ All Nucleus classes are canonical (scanned ${scanned} file${scanned === 1 ? "" : "s"}).`);
    return 0;
  }
  if (warnings) console.error(`⚠ ${warnings} compatibility warning${warnings === 1 ? "" : "s"}.`);
  if (errors) console.error(`✗ ${errors} unknown Nucleus class${errors === 1 ? "" : "es"} in ${scanned} scanned file(s).`);
  return errors ? 1 : 0;
}

async function cmdAiContext(args) {
  const llms = join(PKG_ROOT, "llms.txt");
  const text = existsSync(llms)
    ? await readFile(llms, "utf8")
    : "llms.txt not found. Run `npm run build`.";
  const outIndex = args.indexOf("--out");
  if (outIndex !== -1 && args[outIndex + 1]) {
    const dest = resolve(process.cwd(), args[outIndex + 1]);
    await mkdir(dirname(dest), { recursive: true });
    await writeFile(dest, text);
    console.log(`✓ Wrote AI context to ${relative(process.cwd(), dest)}`);
  } else {
    process.stdout.write(text);
  }
  return 0;
}

async function ensureAiContext() {
  const contextDir = resolve(process.cwd(), ".nucleus");
  const contextPath = join(contextDir, "ai-context.md");
  const llms = join(PKG_ROOT, "llms.txt");
  const context = existsSync(llms) ? await readFile(llms, "utf8") : "";
  await mkdir(contextDir, { recursive: true });
  await writeFile(contextPath, context);
  return contextPath;
}

async function writeGeneratedRule(path, content) {
  if (existsSync(path)) {
    const existing = await readFile(path, "utf8");
    if (!existing.includes(GENERATED_RULE_MARKER)) {
      throw new Error(`Refusing to replace existing ${relative(process.cwd(), path)}. Move it or merge the Nucleus rules manually.`);
    }
  }
  await mkdir(dirname(path), { recursive: true });
  await writeFile(path, content);
}

async function writeToolRule(tool) {
  const root = process.cwd();
  if (tool === "claude") {
    const path = resolve(root, ".claude/rules/nucleus.md");
    const content = `---
paths:
  - "**/*.{html,htm,jsx,tsx,js,ts,vue,svelte,astro,php,erb}"
---
${GENERATED_RULE_MARKER}
${NUCLEUS_RULES}`;
    await writeGeneratedRule(path, content);
    return path;
  }

  if (tool === "cursor") {
    const path = resolve(root, ".cursor/rules/nucleus.mdc");
    const content = `---
description: Use real Nucleus CSS classes for UI markup
globs: ["**/*.{html,htm,jsx,tsx,js,ts,vue,svelte,astro,php,erb}"]
alwaysApply: true
---
${GENERATED_RULE_MARKER}
${NUCLEUS_RULES}`;
    await writeGeneratedRule(path, content);
    return path;
  }

  if (tool === "antigravity") {
    const path = resolve(root, ".agents/rules/nucleus.md");
    const content = `${GENERATED_RULE_MARKER}
${NUCLEUS_RULES}
Reference: @/.nucleus/ai-context.md
`;
    await writeGeneratedRule(path, content);
    return path;
  }

  const path = resolve(root, "AGENTS.md");
  const start = "<!-- nucleus-css:start -->";
  const end = "<!-- nucleus-css:end -->";
  const block = `${start}\n${NUCLEUS_RULES}${end}\n`;
  const existing = existsSync(path) ? await readFile(path, "utf8") : "";
  const startIndex = existing.indexOf(start);
  const endIndex = existing.indexOf(end, startIndex + start.length);
  if (startIndex !== -1 && endIndex !== -1) {
    const updated = `${existing.slice(0, startIndex)}${block}${existing.slice(endIndex + end.length).replace(/^\n?/, "")}`;
    await writeFile(path, updated);
  } else {
    const separator = existing && !existing.endsWith("\n") ? "\n\n" : existing ? "\n" : "";
    await writeFile(path, `${existing}${separator}${block}`);
  }
  return path;
}

async function cmdAiSetup(args) {
  const requested = (args[0] || "").toLowerCase();
  const tool = AI_TOOL_ALIASES.get(requested);
  if (!tool) {
    console.error("Choose an AI tool: claude, codex, cursor, or antigravity.");
    return 1;
  }

  const contextPath = await ensureAiContext();
  const rulePath = await writeToolRule(tool);
  console.log(`✓ Wrote ${relative(process.cwd(), contextPath)}`);
  console.log(`✓ Configured ${tool} in ${relative(process.cwd(), rulePath)}`);
  console.log("\nValidate AI-generated markup with:");
  console.log("  npx @navital/nucleus-css validate ./src ./app ./components ./pages");
  return 0;
}

async function cmdInit() {
  const pkg = await readPkg();
  const configPath = resolve(process.cwd(), "nucleus.config.js");
  const contextDir = resolve(process.cwd(), ".nucleus");
  const contextPath = join(contextDir, "ai-context.md");

  const config = `// Nucleus CSS project configuration.
// Consumed by \`npx ${pkg.name} validate\` and AI tooling.
export default {
  package: ${JSON.stringify(pkg.name)},
  prefix: "n-",
  // Directories to validate for unknown Nucleus classes.
  include: ["src", "app", "components", "pages"],
  // Extra project class prefixes the validator should ignore.
  ignore: [],
};
`;

  const llms = join(PKG_ROOT, "llms.txt");
  const context = existsSync(llms) ? await readFile(llms, "utf8") : "";

  if (existsSync(configPath)) {
    console.log("• nucleus.config.js already exists — leaving it untouched.");
  } else {
    await writeFile(configPath, config);
    console.log("✓ Created nucleus.config.js");
  }
  await mkdir(contextDir, { recursive: true });
  await writeFile(contextPath, context);
  console.log("✓ Created .nucleus/ai-context.md");
  console.log("\nPoint your AI assistant at .nucleus/ai-context.md, then run:");
  console.log(`  npx ${pkg.name} validate ./src`);
  return 0;
}

async function cmdManifest() {
  const manifest = await loadManifest();
  process.stdout.write(JSON.stringify(manifest, null, 2) + "\n");
  return 0;
}

async function cmdSearch(args) {
  const manifest = await loadManifest();
  const query = (args[0] || "").replace(/^\./, "").toLowerCase();
  const matches = manifest.classes.filter((entry) =>
    !query || entry.name.includes(query) || entry.description.toLowerCase().includes(query),
  );
  process.stdout.write(JSON.stringify(matches, null, 2) + "\n");
  return matches.length ? 0 : 1;
}

async function cmdComponent(args) {
  const manifest = await loadManifest();
  const requested = (args[0] || "").replace(/^\./, "");
  const component = manifest.components.find((entry) => entry.name === requested);
  if (!component) {
    console.error(`Unknown Nucleus component: ${requested || "<missing>"}`);
    return 1;
  }
  process.stdout.write(JSON.stringify(component, null, 2) + "\n");
  return 0;
}

async function cmdToken(args) {
  const manifest = await loadManifest();
  const requested = args[0] || "";
  const tokens = requested
    ? manifest.tokens.filter((entry) => entry.name === requested || entry.name.includes(requested))
    : manifest.tokens;
  process.stdout.write(JSON.stringify(tokens, null, 2) + "\n");
  return tokens.length ? 0 : 1;
}

async function cmdDeprecated() {
  const manifest = await loadManifest();
  process.stdout.write(JSON.stringify(manifest.classes.filter((entry) => entry.deprecated), null, 2) + "\n");
  return 0;
}

async function help() {
  const pkg = await readPkg();
  console.log(`${pkg.name} v${pkg.version} — CLI

Usage:
  npx ${pkg.name} <command> [options]

Commands:
  validate [paths...]   Scan HTML/JSX/TSX/Vue/Svelte for unknown Nucleus classes
                        (defaults to the current directory). Exits non-zero on
                        any unknown class.
  ai-context [--out f]  Print the compact AI context (llms.txt) or write it to a
                        file with --out.
  ai-setup <tool>       Configure Claude Code, Codex, Cursor, or Antigravity with
                        Nucleus context and native project rules.
  init                  Create nucleus.config.js and .nucleus/ai-context.md.
  manifest              Print the machine-readable class manifest as JSON.
  search [query]        Search classes and return structured JSON metadata.
  component <n-name>    Return one component contract and example as JSON.
  token [name]          Return token values and categories as JSON.
  deprecated            List deprecated aliases and their replacements as JSON.
  help                  Show this help.
  --version             Print the package version.

Examples:
  npx ${pkg.name} validate ./src
  npx ${pkg.name} ai-context --out .nucleus/ai-context.md
  npx ${pkg.name} ai-setup claude
  npx ${pkg.name} ai-setup codex
  npx ${pkg.name} ai-setup cursor
  npx ${pkg.name} ai-setup antigravity
  npx ${pkg.name} search flex
  npx ${pkg.name} component n-navbar
  npx ${pkg.name} token --n-space-4
  npx ${pkg.name} deprecated
`);
  return 0;
}

async function main() {
  const [command, ...args] = process.argv.slice(2);
  switch (command) {
    case "validate":
      process.exit(await cmdValidate(args));
      break;
    case "ai-context":
      process.exit(await cmdAiContext(args));
      break;
    case "ai-setup":
      process.exit(await cmdAiSetup(args));
      break;
    case "init":
      process.exit(await cmdInit());
      break;
    case "manifest":
      process.exit(await cmdManifest());
      break;
    case "search":
      process.exit(await cmdSearch(args));
      break;
    case "component":
      process.exit(await cmdComponent(args));
      break;
    case "token":
      process.exit(await cmdToken(args));
      break;
    case "deprecated":
      process.exit(await cmdDeprecated());
      break;
    case "--version":
    case "-v": {
      const pkg = await readPkg();
      console.log(pkg.version);
      process.exit(0);
      break;
    }
    case undefined:
    case "help":
    case "--help":
    case "-h":
      process.exit(await help());
      break;
    default:
      console.error(`Unknown command: ${command}\n`);
      await help();
      process.exit(1);
  }
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
